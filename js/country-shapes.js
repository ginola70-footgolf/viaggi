/**
 * Restituisce il path SVG per una nazione, normalizzato.
 * @param {string} name - nome nazione (minuscolo, senza accenti)
 */
window.getCountryShape = function(name) {
  const key = normalizeKey(name);
  return window.COUNTRY_SHAPES[key] || window.COUNTRY_SHAPES['default'];
};

function normalizeKey(str) {
  return str.toLowerCase()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
    .replace(/\s+/g, '_');
}
